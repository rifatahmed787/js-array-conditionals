// single comparison in javascript

var num1 = 10;
var num2 = 15;

console.log(num1 < num2);
console.log(num1 > num2);
console.log(num1 === num2);
console.log(num1 <= num2);
console.log(num1 >= num2);
console.log(num1 !== num2);



// multiple comparison with logical operator

var number1 = 12;
var number2 = 13;
var number3 = 14;

console.log(number1 < number2 && number1 < number3 && number2 < number3);

console.log(number1 > number2 || number1 > number3 || number3 > number2);